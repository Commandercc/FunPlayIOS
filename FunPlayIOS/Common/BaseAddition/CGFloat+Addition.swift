import CoreGraphics
import UIKit

extension CGFloat {
    static var screenWidth: CGFloat { return UIScreen.main.bounds.width }
    static var screenHeight: CGFloat { return UIScreen.main.bounds.height }
    static var screenScale: CGFloat { return UIScreen.main.scale }
}
